package fr.logica.jsf.components.schedule;

/**
 * Available views for the schedule component.
 */
public enum ScheduleView {

	/** Month view. */
	month,
	/** Week view (without any time slot). */
	basicWeek,
	/** Day view (without any time slot). */
	basicDay,
	/** Week view. */
	agendaWeek,
	/** Day view. */
	agendaDay;

}
